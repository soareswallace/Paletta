// Pure RAL Classic matching — no UIKit dependency, Linux-testable via PalettaCore

struct RALColor {
    let code: String
    let name: String
    let r: UInt8
    let g: UInt8
    let b: UInt8
}

func nearestRALColor(r: Float, g: Float, b: Float) -> RALColor {
    ralDatabase.min { ralDistSq($0, r, g, b) < ralDistSq($1, r, g, b) }!
}

func ralDistSq(_ c: RALColor, _ r: Float, _ g: Float, _ b: Float) -> Float {
    let dr = Float(c.r) - r
    let dg = Float(c.g) - g
    let db = Float(c.b) - b
    return dr*dr + dg*dg + db*db
}

private func ral(_ code: String, _ name: String, _ r: UInt8, _ g: UInt8, _ b: UInt8) -> RALColor {
    RALColor(code: code, name: name, r: r, g: g, b: b)
}

